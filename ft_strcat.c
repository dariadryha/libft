#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int t;
	int k;

	k = 0;
	t = ft_strlen(s1);
	while (s2[k] != '\0')
		s1[t++] = s2[k++];
	s1[t] = '\0';
	return (s1);
}
